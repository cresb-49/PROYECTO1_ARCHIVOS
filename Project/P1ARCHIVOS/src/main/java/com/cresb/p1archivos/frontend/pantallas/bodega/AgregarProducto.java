    private void cargarId() {
        try {
            var id = this.productoRepository.lastId();
            //Eliminamos la P y obtenermos el valro decimal
            int valor = Integer.parseInt(id.substring(1));
            //Generamos el nuevo ID
            String newId = String.format("P%04d", (valor+1));
            this.fieldId.setText(newId);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se puede generar el nuevo ID", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
