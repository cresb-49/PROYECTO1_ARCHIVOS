    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String id = this.fieldId.getText();
            String nombre = this.fieldNombre.getText();
            String marca = this.fieldMarca.getText();
            double valor = Double.valueOf(this.fieldValor.getText());
            String des = textDes.getText();
            int cantidad = Integer.parseInt(this.jSpinner1.getValue().toString());

            //Generacion del objeto
            Producto producto = new Producto(id,nombre,marca,valor,des);
            //Asignacion de la existencia en la base de datos
            Bodega bodega = new Bodega(producto,cantidad);

            //Verificacion de la informacion
            this.verificarInformacion(producto,bodega);

            //Registro del producto
            this.productoRepository.guardarProducto(producto);
            //Registro en la bodega
            this.bodegaRepository.save(bodega);

            JOptionPane.showMessageDialog(this, "Se registro con exito el producto", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(this, "No se puede registrar el producto en la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }catch (InformacionIncompletaException ex){
            JOptionPane.showMessageDialog(this, "Incosistencia en la informacion asignada:\n"+ex.getMessage(), "Error de campos", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Debe de agregar un numero en valor y cantidad:\n"+ex.getMessage(), "Error de campos", JOptionPane.ERROR_MESSAGE);
        } catch (HeadlessException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void verificarInformacion(Producto producto, Bodega bodega) throws InformacionIncompletaException {
        StringBuilder stringBuilder = new StringBuilder();
        if(producto.getId().isBlank()||producto.getId().isEmpty()){
            stringBuilder.append("\t-Se debe de agregar un codigo al producto ejemplo: P000X\n");
        }
        if(producto.getNombre().isBlank()||producto.getNombre().isEmpty()){
            stringBuilder.append("\t-Se debe de agregar nombre al producto\n");
        }
        if(producto.getMarca().isBlank()||producto.getMarca().isEmpty()){
            stringBuilder.append("\t-Se debe de agregar marca al producto\n");
        }
        if(producto.getDescripcion().isBlank()||producto.getDescripcion().isEmpty()){
            stringBuilder.append("\t-Se debe de agregar descripcion al producto\n");
        }
        if(producto.getValor() <= 0){
            stringBuilder.append("\t-El valor del porducto debe de ser mayor o igual a Q. 1.00\n");
        }
        if(bodega.getCantidad()<1){
            stringBuilder.append("\t-La cantidad del producto debe ser mayor o igual a 1\n");
        }
        var result = stringBuilder.toString();
        if(!(result.isEmpty()||result.isBlank())){
            throw  new InformacionIncompletaException(result);
        }
    }

